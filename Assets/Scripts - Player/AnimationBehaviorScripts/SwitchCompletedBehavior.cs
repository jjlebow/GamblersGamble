public class SwitchCompletedBehavior : StateMachineBehaviour
{
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        StateManager.instance.stance = !StateManager.instance.stance;
        if(StateManager.instance.stance == false)     //this will switch the animator to the appropriate layer
        {
            animator.SetLayerWeight(1,0);
        }
        else   
            animator.SetLayerWeight(1,1);
